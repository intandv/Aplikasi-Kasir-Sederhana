     private void AmbilData() {
        // Format tanggal dari JDateChooser
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String tanggal = sdf.format(jDateChooser1.getDate());
        
        // Query untuk mengambil data
        String query =  "SELECT Tanggal, SUM(Total_Beli) AS TotalPendapatan, SUM(Jumlah) AS TotalPenjualan " +
                       "FROM penjualan WHERE Tanggal = ? GROUP BY Tanggal";
        
        try (Connection conn = Koneksi.getKoneksi();
             PreparedStatement stmt = conn.prepareStatement(query)) {
             
            stmt.setString(1, tanggal);
            ResultSet rs = stmt.executeQuery();
            
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); // Menghapus data lama di JTable

            while (rs.next()) {
                String tgl = rs.getString("Tanggal");
                String totalPendapatan = rs.getString("TotalPendapatan");
                String totalPenjualan = rs.getString("TotalPenjualan");

                // Tambahkan data ke JTable
                model.addRow(new Object[]{tgl, totalPendapatan, totalPenjualan});

                // Tampilkan ke TextField
                txtPendapatan.setText(totalPendapatan);
                txtPenjualan.setText(totalPenjualan);
            }
            // Menampilkan data di JTable
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal mengambil data: " + e.getMessage());
        }
    }
