    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
       // Koneksi ke database
    String url = "jdbc:mysql://localhost:3306/dbrajoseblak"; // Sesuaikan dengan konfigurasi Anda
    String user = "root"; // Username database Anda
    String password = ""; // Password database Anda
    
    Connection conn = null;
    PreparedStatement pst = null;
    
    try {
        // Inisialisasi koneksi
        conn = DriverManager.getConnection(url, user, password);
        
        // Query untuk menyisipkan data
        String query = "INSERT INTO laporan_penjualan (Tanggal, Total_Pendapatan, Jumlah_Penjualan) VALUES (?, ?, ?)";
        pst = conn.prepareStatement(query);
        
        // Ambil model JTable
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        // Iterasi setiap baris pada JTable
        for (int i = 0; i < model.getRowCount(); i++) {
            String tanggal = model.getValueAt(i, 0).toString();
            String totalPendapatan = model.getValueAt(i, 1).toString();
            String jumlahPenjualan = model.getValueAt(i, 2).toString();
            
            // Set parameter untuk PreparedStatement
            pst.setString(1, tanggal);
            pst.setString(2, totalPendapatan);
            pst.setString(3, jumlahPenjualan);
            
            // Eksekusi query
            pst.executeUpdate();
        }
        
        // Tampilkan pesan sukses
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan ke database!");
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
    } finally {
        try {
            if (pst != null) pst.close();
            if (conn != null) conn.close();
        } catch (Exception ex) {
            ex.printStackTrace();}}
        
    
    }//GEN-LAST:event_btnsimpanActionPerformed
    
